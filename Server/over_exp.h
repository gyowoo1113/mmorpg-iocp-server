#pragma once
#include "pch.h"

enum COMP_TYPE { OP_ACCEPT, OP_RECV, OP_SEND, EV_MOVE, EV_HEAL, EV_ATTACK, EV_ATTACK_ACTIVE, EV_RESPAWN 
	, EV_SKILL_COOL, EV_SKILL_RELEASE, length };
class OVER_EXP {
public:
	WSAOVERLAPPED _over;
	WSABUF _wsabuf;
	char _send_buf[BUF_SIZE];
	COMP_TYPE _comp_type;
	int target_id;
	OVER_EXP()
	{
		_wsabuf.len = BUF_SIZE;
		_wsabuf.buf = _send_buf;
		_comp_type = OP_RECV;
		ZeroMemory(&_over, sizeof(_over));
	}
	OVER_EXP(char* packet)
	{
		_wsabuf.len = packet[0];
		_wsabuf.buf = _send_buf;
		ZeroMemory(&_over, sizeof(_over));
		_comp_type = OP_SEND;
		memcpy(_send_buf, packet, packet[0]);
	}
};

